class Gallery extends Component {
  state = {ID: 0}

  onChangeId = newId => {
    this.setState({ID: newId})
  }

  render() {
    const {ID} = this.state
    const filterListImg = imagesList.filter(each => each.id === ID)
    const ImgURL = filterListImg[0].imageUrl
    return (
      <div className="main-container">
        <div className="image-container">
          <img
            src={ImgURL}
            className="image-resize"
            alt={filterListImg[0].imageAltText}
          />
        </div>
        <div className="content">
          <h1 className="heading">Nature Photography</h1>
          <p className="description">Nature Photography by Rahul</p>
          <ul>
            {imagesList.map(each => (
              <ThumbnailItem
                details={each}
                key={each.id}
                changeImg={this.onChangeId}
                isActive={each.id === ID}
              />
            ))}
          </ul>
        </div>
      </div>
    )
  }
}

export default Gallery
